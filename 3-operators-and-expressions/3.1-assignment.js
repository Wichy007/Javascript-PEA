// This file provides examples of assignment operators in JavaScript.

let x = 10;

// Addition assignment.
x += 5; // equivalent to x = x + 5
console.log(x); // 15

// Subtraction assignment.
x -= 5; // equivalent to x = x - 5
console.log(x); // 10

// Multiplication assignment.
x *= 5; // equivalent to x = x * 5
console.log(x); // 50

// Division assignment.
x /= 5; // equivalent to x = x / 5
console.log(x); // 10

// Remainder assignment.
x %= 3; // equivalent to x = x % 3
console.log(x); // 1

// Exponentiation assignment.
x **= 2; // equivalent to x = x ** 2
console.log(x); // 1