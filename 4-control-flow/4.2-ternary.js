// This file provides examples of the ternary operator in JavaScript.

// The ternary operator is a shorthand for an if-else statement.
const age = 20;
const isAdult = age >= 18 ? "You are an adult." : "You are not an adult.";
console.log(isAdult);

// Another example of the ternary operator.
const temperature = 25;
const weather = temperature > 30 ? "It's a hot day." : "It's a pleasant day.";
console.log(weather);

// A nested ternary operator.
const score = 85;
const grade = score >= 90 ? "A" : score >= 80 ? "B" : score >= 70 ? "C" : "D";
console.log(grade);