// This file provides examples of arithmetic operators in JavaScript.

let a = 10;
let b = 5;

// Addition
console.log(a + b); // 15

// Subtraction
console.log(a - b); // 5

// Multiplication
console.log(a * b); // 50

// Division
console.log(a / b); // 2

// Remainder
console.log(a % b); // 0

// Exponentiation
console.log(a ** b); // 100000

// Increment operators
let c = 10;
console.log(c++); // 10 (post-increment)
console.log(c);   // 11

let d = 10;
console.log(++d); // 11 (pre-increment)
console.log(d);   // 11

// Decrement operators
let e = 10;
console.log(e--); // 10 (post-decrement)
console.log(e);   // 9

let f = 10;
console.log(--f); // 9 (pre-decrement)
console.log(f);   // 9